        $('#login-form').submit(function (e) {
            e.preventDefault();
            var form = $(this);
            var url = form.attr('action');
            $.ajax({
                type: 'POST',
                url: url,
                data: form.serialize(),
                success: function (data) {
                    alert(data.message);
                    window.location.href = '{{ route('admin.v1.dashboard') }}';
                }
            });
        });
    });
